public class bit_manipulation_01 {

    /// This is the code for the binary operators
    public static void main(String[] args) {
        System.out.println(5 & 6); ///------> This is the code to binary and
        System.out.println(5 | 6); ///------> This is the code to binary or
        System.out.println(~5);  ///-----> This is the code to binary not
        System.out.println(5<<2); ///------> This is the code for binary left shift
        System.out.println(6>>1); ///------> This is the code for binary Right shift
    }
    
}
